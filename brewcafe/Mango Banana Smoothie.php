        <table class=" container table col-md-9 mt-2 table-striped ">
          <thead>
           <tr>
               <th>PROCEDURE</th>                   
           </tr>
           </thead>
           <tr>
            <th>1 Prepare the Ingredients:</th>
            <td>Peel and chop the ripe banana into chunks.
               And If you haven't already, freeze the mango chunks. This adds a nice thickness and chill to the smoothie</td>
           </tr>
           <tr>
            <th>2 Blend the Fruits:</th>
            <td>In a blender, combine the frozen mango chunks, banana chunks, plain yogurt, and milk.</td>
           </tr>
           <tr>
            <th>3 Sweeten to Taste:</th>
            <td>Add honey or maple syrup to the blender if you desire extra sweetness. You can adjust the quantity based on your preference.</td>
           </tr>
           <tr>
            <th>4 Blend Until Smooth:</th>
            <td>Blend all the ingredients until smooth and creamy. If the smoothie is too thick, you can add more milk until you reach your desired consistency.</td>
           </tr>
           <tr>
            <th>5 Adjust Consistency:</th>
            <td>If the smoothie is too thin, you can add a few ice cubes and blend again until you achieve the desired thickness.</td>
           </tr>
           <tr>
            <th>6 Serve:</th>
            <td>Pour the smoothie into glasses and serve immediately. You can optionally garnish with additional slices of fresh mango or banana.</td>
           </tr>
           <tr>
