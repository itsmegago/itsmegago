           <tr>
            <th>3</th>
            <td>1/2 cup plain yogurt (Greek yogurt or any plant-based alternative)</td>
           </tr>
           <tr>
            <th>4</th>
            <td>1/2 cup milk (dairy or plant-based)</td>
           </tr>
           <tr>
