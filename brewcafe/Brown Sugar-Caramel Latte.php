        <table class=" container table col-md-9 mt-2 table-striped ">
          <thead>
           <tr>
               <th>PROCEDURE</th>                   
           </tr>
           </thead>
           <tr>
            <th>1 Brown Sugar Syrup:</th>
            <td><ul>
               <li>In a small saucepan, combine brown sugar and water.</li>
               <li>Heat over medium heat, stirring until the sugar is completely dissolved.</li>
               <li>Allow the syrup to simmer for a few minutes until it thickens slightly.</li>
               <li>Remove from heat and let it cool.</li>
            </ul></td>
           </tr>
           <tr>
            <th>2 Brew Coffee or Espresso:</th>
            <td><ul><li>Brew 1-2 shots of espresso or make a strong cup of coffee using your preferred method.</li></ul></td>
           </tr>
           <tr>
            <th>3  Heat Milk:</th>
            <td><ul><li>In a separate saucepan or using a milk frother, heat the milk until it's hot but not boiling.</li></ul></td>
           </tr>
           <tr>
            <th>4 Assemble the Latte:</th>
            <td><ul>
              <li>Pour the brewed coffee or espresso into your favorite mug. </li>
              <li>Add the brown sugar syrup to the coffee. </li>
              <li>Froth the hot milk and pour it over the coffee and syrup mixture. </li>
          </ul></td>
           </tr>
           <tr>
            <th>5 Add Caramel Sauce:</th>
            <td><ul><li>Drizzle caramel sauce over the top of the milk foam.</li></ul></td>
           </tr>
           <tr>
            <th>6 Optional: Whipped Cream:</th>
            <td><ul><li>If desired, top your latte with a dollop of whipped cream.</li></ul></td>
           </tr>
           <tr>
            <th>7 Enjoy:</th>
            <td><ul>
              <li>Stir the latte gently to mix the flavors.</li>
              <li>Your homemade Brown Sugar Caramel Latte is ready to be enjoyed!</li>
             </ul></td>
           </tr>
