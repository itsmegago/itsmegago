           <tr>
            <th>1 Brew and Cool the Coffee:</th>
            <td><ul><li>Brew a strong cup of coffee. Allow it to cool to room temperature, and then refrigerate or use ice to speed up the cooling process.</li></ul></td>
           </tr>
           <tr>
            <th>2 Prepare the Caramel Sauce:</th>
            <td><ul>
              <li>If you don't have store-bought caramel sauce, you can make a simple one by melting sugar in a saucepan until it turns into a golden-brown liquid. Add a bit of butter and cream to create a smooth caramel sauce</li>
            </ul></td>
            </tr>
           <tr>
            <th>3 Blend the Ingredients:</th>
            <td><ul><li>In a blender, combine the cooled coffee, milk, caramel sauce, sugar, and ice cubes.</li></ul></td>
           </tr>
           <tr>
            <th>4 Blend Until Smooth:</th>
            <td><ul><li>Blend the ingredients until you achieve a smooth and slushy consistency.</li></ul></td>
           </tr>
           <tr>
            <th>5 Taste and Adjust:</th>
            <td><ul><li>Taste the frozen caramel latte and adjust the sweetness by adding more sugar or caramel sauce if needed. Blend again if you make any adjustments.</li></ul></td>
           </tr>
           <tr>
            <th>6 Serve:</th>
            <td><ul><li>Pour the frozen caramel latte into glasses.</li></ul></td>
           </tr>
           <tr>
            <th>7 Top with Whipped Cream and Caramel:</th>
            <td><ul><li>If desired, top each glass with a dollop of whipped cream and drizzle additional caramel sauce over the whipped cream.</li></ul></td>
           </tr>
