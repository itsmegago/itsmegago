           </thead>
           <tr>
            <th>1 Brew the Coffee:</th>
            <td><ul>
              <li>Brew a strong cup of coffee using your preferred method. If you have an espresso machine, you can also use it to make a shot of espresso.</li>
            </ul>
          </td>
           </tr>
           <tr>
            <th>2Heat the Milk:</th>
            <td><ul>
              <li>Heat the milk in a small saucepan over medium heat until it's hot but not boiling. Alternatively, you can heat the milk in the microwave.</li>
            </ul>
          </td>
           </tr>
           <tr>
            <th>3 Froth the Milk:</th>
            <td><ul>
              <li>Use a milk frother to froth the hot milk until it has a creamy consistency. </li>
                <li>If you don't have a frother, you can shake the hot milk vigorously in a jar or use a French press to create foam.</li>
            </ul>
          </td>
           </tr>
           <tr>
            <th>4 Combine Coffee and Milk:</th>
            <td><ul><li>Pour the brewed coffee or espresso into a coffee cup.</li>
