          <thead>
           <tr>
               <th>PROCEDURE</th>                   
           </tr>
           </thead>
           <tr>
            <th>1 Brew Espresso or Coffee:</th>
            <td><ul>
              <li>Brew 1-2 shots of espresso or make a strong cup of coffee using your preferred method.</li>
            </ul>
          </td>
           </tr>
           <tr>
            <th>2 Prepare Vanilla Syrup:</th>
            <td><ul>
              <li>If you're not using store-bought vanilla syrup, prepare it by mixing equal parts sugar and water and adding vanilla extract. Stir until the sugar dissolves.</li>
          </ul>
        </td>
           </tr>
           <tr>
            <th>3 Heat and Froth Milk:</th>
            <td><ul>
              <li>Heat the milk on the stovetop or in the microwave until hot but not boiling. </li> 
                <li>Froth the milk using a frother or by shaking it vigorously in a jar.</li> 
              </ul>
            </td>
           </tr>
           <tr>
            <th>4 Assemble the Drink:</th>
            <td><ul>
              <li>Fill a glass with ice cubes.</li>
                 <li>And Pour the brewed espresso or coffee over the ice.</li>
               </ul> 
              </td>
           </tr>
           <tr>
            <th>5 Add Vanilla Syrup:</th>
            <td><ul>
              <li>Add 1-2 tablespoons of vanilla syrup to the coffee. Adjust the sweetness according to your taste.</li>
          </ul>
        </td>
           </tr>
           <tr>
            <th>6 Pour Frothed Milk:</th>
            <td><ul>
              <li>Pour the frothed milk over the coffee and ice. Hold back the foam with a spoon to let the liquid milk mix with the coffee.</li>
            </ul>
          </td>
           </tr>
           <tr>
            <th>7 Drizzle Caramel Sauce:</th>
            <td><ul>
              <li>Drizzle caramel sauce over the top of the frothed milk.</li>
            </ul>
          </td>
           </tr>
           <tr>
            <th>8 (Optional) Top with More Caramel:</th>
